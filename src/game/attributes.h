/// Defines the way an attribute works.
enum class AttributeMode : int8_t {
  /// Attribute is "held" by the object, can decay. Used for gold mines, sheep,
  /// etc.
  Keep = 0b00000,
  /// Attribute is given to owner when the object spawns.
  Give = 0b00001,
  /// Attribute is given to owner when the object spawns, and taken away when
  /// the object dies.
  GiveAndTake = 0b00010,
  /// Attribute is given to owner when the object (building) _completes_, and
  /// taken away when the object dies.
  GiveLateAndTake = 0b00100,
  /// Attribute is given to owner when the object (building) _completes_.
  /// UserPatch addition.
  GiveLate = 0b01000,
  /// Attribute is given to owner when the object spawns, and taken away after
  /// the death animation completes.
  /// Community Patch addition.
  GiveAndTakeLate = 0b10000,
};
